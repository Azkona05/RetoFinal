package modelo;

/**
 * @author An Azkona, Ander Arilla, Nora Yakoubi, Maleck Benigno
 * Enumeración que representa las distintas posiciones posibles de un jugador.
 */
public enum EnumPosicion {

    /**
     * Quarterback: posición encargada de liderar el ataque y pasar el balón.
     */
    QUARTERBACK,

    /**
     * Tackle: jugador de la línea ofensiva o defensiva, encargado de bloquear o derribar.
     */
    TACKLE,

    /**
     * Running back: jugador ofensivo especializado en correr con el balón.
     */
    RUNNING,

    /**
     * Guard: jugador de la línea ofensiva que protege al quarterback y abre espacios.
     */
    GUARD;
}
